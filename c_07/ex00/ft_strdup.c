#include <stdlib.h>

int			ft_strlen(char *str)
{
	int		i;

	i = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	return (i);
}

char		*ft_strcpy(char *dest, char *src)
{
	int		index;

	index = 0;
	while (src[index] != '\0')
	{
		dest[index] = src[index];
		index++;
	}
	dest[index] = src[index];
	return (dest);
}

char		*ft_strdup(char *src)
{
	char	*dup;

	if (!(dup = malloc(sizeof(char) * (ft_strlen(src)) + 1)))
		return (0);
	return (ft_strcpy(dup, src));
}
