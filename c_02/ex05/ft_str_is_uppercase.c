int		ft_str_is_uppercase(char *str)
{
	int i;
	int ctr;

	i = 0;
	ctr = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
		{
			ctr++;
		}
		else
		{
			ctr--;
		}
		i++;
	}
	if (ctr < i)
	{
		return (0);
	}
	else
	{
		return (1);
	}
}
