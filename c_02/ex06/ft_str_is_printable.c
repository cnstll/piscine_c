int		ft_str_is_printable(char *str)
{
	int i;
	int ctr;

	i = 0;
	ctr = 0;
	while (str[i] != '\0')
	{
		if (str[i] > 31 && str[i] < 127)
		{
			ctr++;
		}
		else
		{
			ctr--;
		}
		i++;
	}
	if (ctr < i)
	{
		return (0);
	}
	else
	{
		return (1);
	}
}
