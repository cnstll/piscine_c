void	ft_rev_int_tab(int *tab, int size)
{
	int i;
	int j;
	int t_tab[size];

	j = 0;
	i = 0;
	while (j < size)
	{
		t_tab[j] = tab[i];
		i++;
		j++;
	}
	i = 0;
	j = size - 1;
	while (i < size)
	{
		tab[i] = t_tab[j];
		j--;
		i++;
	}
}
