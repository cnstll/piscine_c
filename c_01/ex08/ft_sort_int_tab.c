void	ft_sort_int_tab(int *tab, int size)
{
	int i;
	int j;
	int t1;
	int t2;

	i = 0;
	while (i < size)
	{
		j = i + 1;
		while (j < size)
		{
			t1 = tab[i];
			t2 = tab[j];
			if (t1 > t2)
			{
				tab[i] = t2;
				tab[j] = t1;
			}
			j++;
		}
		i++;
	}
}
