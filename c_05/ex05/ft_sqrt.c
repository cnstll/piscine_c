int		ft_sqrt(int nb)
{
	int y;

	y = 1;
	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (1);
	while (y < 46342)
	{
		if (nb / y == y && nb % y == 0)
			return (y);
		y++;
	}
	return (0);
}
